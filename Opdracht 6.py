import math

# De wortel van 16
wortel_van_16 = math.sqrt(16)
print("De wortel van 16 is:", wortel_van_16)

# 2 tot de macht 5
machtsverheffing = math.pow(2, 5)
print("2 tot de macht 5 is:", machtsverheffing)

# De tangens van 10
tangens_van_10 = math.tan(math.radians(10))
print("De tangens van 10 graden is:", tangens_van_10)

# 6.216 naar boven afgerond
afgerond_getal = math.ceil(6.216)
print("6.216 naar boven afgerond is:", afgerond_getal)